def suma(a, b):
    """suma de dos elementos\n
         a + b"""
    return a + b


def resta(a, b):
    """resta de dos elementos\n
         a - b"""
    return a - b


def multiplicacion(a, b):
    """multiplicacion de dos elementos\n
         a * b"""
    return a * b


def division(a, b):
    """division de dos elementos\n
         a / b"""
    return a / b
